def categoria_por_nombre(nombre: str) -> str | None:
    for _pid, p in PRODUCTOS.items():
        if p["nombre"] == nombre:
            return p["categoria"]
    return None


def es_comida_lunch_o_rapida_por_nombre(nombre: str) -> bool:
    c = categoria_por_nombre(nombre)
    return c in ("lunch", "comida_rapida")
